def decipher(str)
  alphabet = ("a".."z").to_a

  str.chars.map do |letter|
    case letter
    when /[A-Z]/ then alphabet.rotate(13)[alphabet.index(letter.downcase)].upcase
    when /[a-z]/ then alphabet.rotate(13)[alphabet.index(letter)]
    else letter
    end
  end.join
end

